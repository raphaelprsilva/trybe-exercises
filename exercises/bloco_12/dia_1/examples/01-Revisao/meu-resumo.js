/* 
  - Um estado, representa um momento de um componente dinâmico.
  Ex. Componente Relógio -> Estado Atual = Hora Atual

  - Então, ESTADO, é um momento que pode mudar, ao logo do tempo

  - É um MOMENTO que eu quero PRESERVAR, enquanto o usuário INTERAGE

  - O React é capaz de mudar o ESTADO de um COMPONENTE, à medida em
    que o usuário INTERAGE;
 */

/*--------------------------------------------------------------------
  - ONDE POSSO GUARDAR INFORMAÇÕES REFERENTES AO ESTADO DO COMPONENTE?
----------------------------------------------------------------------

  - Não posso ter variáveis declaradas dentro de uma Classe;
  - Também não posso 'guardar' esse estado em variáveis FORA da Classe;
  - Dentro da função 'render' também não posso, pois toda vez que algo muda, o componente é renderizado novamente;


  - Cada COMPONENTE terá seu ESTADO, e "enxerga" somente SEU ESTADO
  - Componente 1 -> Estado 1
  - Componente 2 -> Estado 2
*/


